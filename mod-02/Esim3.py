'''
Python-kirjastojen hyödyntämistä
    impoert lauseet täytyy olla enen varsinaista koodia
'''

import math             # matemaattisia toimitntoja
import random           # satunnaislukujen käsittelyyn

pii = math.pi
print(pii)

# muotoiltu tulostus, alkaa ainapienellä f-kirjaimella
print(f"pii 2 desimaalilla {pii:.2f}")

arvottu = random.randint(3 ,15)
print(f"Arvottu kokonaisluku väliltä 3 - 15: {arvottu}")
